    public void veri_gtr(){
        int kontrol=0;
    bgl.model.setColumnIdentifiers(bgl.dgr_kitap);
        table2.setModel(bgl.model);
        bgl.baglanti();
        try (ResultSet rs = bgl.statement.executeQuery("select * from kitaplar where ktp_brkt_nmr='"+t2.getText()+"'"))//Veritabanındaki tabloya bağlandık
        { 
           while(rs.next())
               {
                   kontrol++;
                  for(int i=1;i<=8;i++)
                  {
                      if (i==4) 
                      {
                         row[i-1]=1;  
                      }
                      else 
                      {
                        row[i-1] = rs.getString(i);  
                      }
                  }                                    
               }  
            if (kontrol!=0) 
            {
                bgl.model.addRow(row);                
                bgl.con.close();
            } 
            else {
                 JOptionPane.showMessageDialog(null,bgl.kitap_yok);
            }
           
        }
        catch (SQLException ex) {
            Logger.getLogger(ktp_odnc_verme.class.getName()).log(Level.SEVERE, null, ex);
        } 
}
