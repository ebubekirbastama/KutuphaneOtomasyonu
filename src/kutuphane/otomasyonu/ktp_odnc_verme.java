    private void btn_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kaydetActionPerformed
         if (table1.getColumnCount()==0||table2.getColumnCount()==0)
         {
         JOptionPane.showMessageDialog(null,bgl.text_kntrl_1);
         }
         else
         {
              try       
              {  
                  for (int i = 0; i < table2.getRowCount(); i++)
                  {
                    Object isim = (Object) table1.getModel().getValueAt(0,1);
                    Object idi = (Object) table2.getModel().getValueAt(i,1);
                    bgl.baglanti(); 
                    CallableStatement calstat=bgl.con.prepareCall("{call ktp_odunc_verme(?,?)}");
                    calstat.setString("ogr_tc_no_",isim.toString());
                    calstat.setString("ktp_brkt_nmr_",idi.toString());
                    calstat.executeQuery();
                    bgl.con.close();
                    calstat.close();  
                  }
                  JOptionPane.showMessageDialog(null,bgl.ekle_mesaj);
                  for (int i = 0; i < table2.getRowCount(); i++)
                  {
                     bgl.model.removeRow(i);
                  }
              }
              catch(Exception e)
              {
                  JOptionPane.showMessageDialog(null, e.getMessage() ,"Hata", 1);
              }  
         }
    }//GEN-LAST:event_btn_kaydetActionPerformed
